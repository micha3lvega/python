cadena = "0123456789"

print(len(cadena))  # Saber el tamaño de la cadena
print(cadena[2])    # Obtiene lo que hay en ese lugar empezando en '0'
print(cadena[0:10]) # indexación de cadenas para seleccionar un rango específico de caracteres
print(cadena[:10])
print(cadena[6:])  # Desde el espacio 10 hasta el final
print(cadena[-1])   # Un numero negativo indica que tomara ese valor, pero comenzando la cuenta en la ultima palabra